/* eslint-disable @typescript-eslint/no-unused-vars */
/** TypeScriptの基本の型 */
// boolean
let bool: boolean = true;

// number
let num: number = 123;

// string
let str: string = "A";

// array
let arr1: Array<number> = [1, 2, 3];
let arr2: number[] = [3, 5, 6, 6];

// tuple
let tuple: [number, string] = [0, "dd"];

// any
let any1: any = false;

// void
const funcA = () => {
  const test = "TEST";
};

// null
let null1: null = null;

// undefined
let undefined1: undefined = undefined;

// object
let obj1: object = {};
let obj2: { id: number; name: string } = { id: 0, name: "aaa" };
